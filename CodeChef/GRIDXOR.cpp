#include "bits/stdc++.h"
using namespace std;

int main() {
	int T; cin>>T;
	while(T--){
	    int n; cin>>n;
	    for(int i=0; i<n; i++){
	        for(int j=0; j<n; j++){
	            cout<<"1 ";
	        }
	        cout<<"\n";
	    }
	}
	return 0;
}

